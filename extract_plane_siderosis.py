     #   center=(cy-80, cx+10, cz),
     #   a=0.7 * args.a, b=0.7 * args.b, c=0.8 * args.c,
     #   siderosis_value=args.siderosis_value,
      #  roughness=0.2, seed=2, angle_deg=30.0
    #)

    # 3rd blob
    #add_ellipsoid_siderosis(
     #   vis, labels, cortex_label,
